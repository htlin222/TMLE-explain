async def generate_audio(scene_name: str, text: str, output_file: str) -> None:
    """生成單個場景的音頻"""
    voice = "zh-TW-HsiaoChenNeural"  # 台灣中文女聲
    communicate = edge_tts.Communicate(text.strip(), voice)
    await communicate.save(output_file)
    print(f"Generated: {output_file}")


async def main() -> None:
    """生成所有場景的音頻"""
    import os

    os.makedirs("audio", exist_ok=True)

    tasks = []
    for scene_name, text in NARRATIONS.items():
        output_file = f"audio/{scene_name}.mp3"
        tasks.append(generate_audio(scene_name, text, output_file))

    await asyncio.gather(*tasks)
    print("All audio files generated!")


if __name__ == "__main__":
    asyncio.run(main())
